int	ft_swapif(long *a, long *b, char todo)
{
	long	temp;

	if (*a > *b)
	{
		if (todo == 'y')
		{
			temp = *a;
			*a = *b;
			*b = temp;
		}
		return (0);
	}
	return (1);
}
